//operator aritmatika
var a = 10;
var b = 5;

console.log(a + b);
console.log(a - b);
console.log(a / b);
console.log(a * b);
console.log(a % b);

//increment dan decrement
var x = 0;
console.log(x);
x++;
console.log(x);
x--;

//operator perbandingan
ab = 10;
ac = 11;

console.log(ab < ac);
console.log(ab > ac);
console.log(ab <= ac);
console.log(ab >= ac);
console.log(ab == ac);
console.log(ab === ac);
console.log(ab != ac);
console.log(ab !== ac);

//operator logika
var y = 10;

console.log(y > 10 && y < 10); // end
console.log(y > 10 || y < 10); // or
console.log(!true); // not (negasi)